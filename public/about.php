<p>In 2004, I therefore founded the <b>The Buildmeister</b> portal (<a
	href="http://www.buildmeister.com">www.buildmeister.com</a>) &ndash; a
community website dedicated to discussing topics related to the build
process. For those of you that don&rsquo;t already know, a <b>Buildmeister</b>
(or Buildmaster) is the name for the individual who is tasked with
defining and implementing the technical build process for a project. The
Buildmeister website has been well received, with many subscribers and
participants. Hopefully it is a useful source of information and can 
help make people better Buildmeisters..."</p>
